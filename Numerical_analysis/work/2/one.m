%计算矩阵的行列式，逆，特征值，特征向量，各种范数，条件数
%a=[4 1 -1;3 2 -6;1 -5 3];
%a=[4 3 1;3 3 -5;1 -5 3];
%a=[5 7 6 5;7 10 8 7;6 8 10 9;5 7 9 10];
a=input('请输入矩阵')
disp('矩阵的行列式')
det(a)
disp('矩阵的逆矩阵')
inv(a)
disp('矩阵的特征值和特征向量')
[v,d]=eig(a)
disp('矩阵的2-范数')
norm(a,2)
disp('矩阵的1-范数')
norm(a,1)
disp('矩阵的无穷-范数')
norm(a,inf)
disp('矩阵的2-条件数')
cond(a,2)
disp('矩阵的1-条件数')
cond(a,1)
disp('矩阵的无穷-条件数')
cond(a,inf)
disp('矩阵的倒条件数')
rcond(a)
